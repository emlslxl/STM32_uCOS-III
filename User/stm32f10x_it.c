/**
  * @brief  This function handles SysTick Handler.
  * @param  None
  * @retval None
  */
void SysTick_Handler(void)
{
	OSIntEnter(); 	 //用于统计中断的嵌套层数，对嵌套层数+1
	OSTimeTick();	   //统计时间，遍历任务，对延时任务计时减1
	OSIntExit();	   //对嵌套层数减1，在退出中断前启动任务调度
}

